"""Классификатор типа задания: инфографика или сюжетная (TZ 8a, 8b.8).

Один маркер «Рис.» на всё, тип определяет бот сам.
По умолчанию — бесплатная эвристика. GPT-классификатор оставлен как
задел на случай неоднозначности, но GPT тоже платный, поэтому в
дефолтный путь не включён (требование экономии, TZ 7.7).
"""

from __future__ import annotations

from .models import SlotType


def classify_type(title: str, bullets: tuple[str, ...] | list[str]) -> SlotType:
    """Эвристика: структурированный перечень из >=2 пунктов -> инфографика.

    Все три блока примера 8Blocks (преимущества инфляции, функции
    блокчейна, список токенов) так уверенно классифицируются как
    инфографика. Картинка-подпись без перечня -> сюжетная.
    """
    meaningful = [b for b in bullets if b and b.strip()]
    if len(meaningful) >= 2:
        return SlotType.INFOGRAPHIC
    return SlotType.STORY
